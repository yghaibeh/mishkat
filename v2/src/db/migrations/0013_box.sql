-- **أوّلُ هجرةِ السلسلة المالية في T31** — على نمط `0003` حرفياً: مفتاحُ التوجيه على كلِّ
-- جدولٍ بلا استثناء (ع-٥ · `db/README.md` الحسم ٢)، ولهجةُ القاسم المشترك (ع-٣):
-- لا `JSONB` · لا فهرسٌ جزئيّ · لا `AUTOINCREMENT` · التاريخُ `INTEGER` · المنطقُ ٠/١.
