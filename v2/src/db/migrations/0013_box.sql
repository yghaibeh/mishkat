-- ### الثابتُ الذي يفرضه هذا المخطط قبل أيّ سطر منطق
-- > **لا عمودَ مبلغٍ ولا عملةٍ ولا رصيدٍ في أيّ جدولٍ هنا** (ق-٦٠).
-- المالُ كلُّه في دفتر النواة؛ وسجلُّ التسليم يحمل **معرّفَ القيد** (`entry_id`) ومنه يُقرأ
-- المبلغُ والعملةُ والسند. فنسخةُ مبلغٍ هنا كانت ستصير **مصدرَ حقيقةٍ ثانياً ينحرف**
-- (المادة ١/٢) — ولذلك يستحيل هنا حتى أن تُكتب. ويحرس ذلك `tests/migrations/box.test.ts`
-- **على المخطط المطبَّق** لا على النيّة، فوقَ الحارس المحتوائيّ القائم على المصدر.
--
-- ### وجدولان **ملحقان فقط** (`appendOnly` في `schema/box.ts` · المادة ٧/٤)
--  · `box_categories`: **الإيقافُ بيانٌ لا حذف** (ق-٦٤) — الفئةُ المُوقَفة تبقى صفّاً
--    (`active = 0`) ليُفرَّق «أُوقفت» عن «لم تُسجَّل قطّ»، ولئلا يتيتّم قيدٌ قديمٌ يشير إليها.
--  · `box_handovers`: **الإقرارُ تحديثٌ على الصفّ نفسِه** (ق-٦١) لا حذفٌ وإدراج — فنمذجتُه
--    حذفاً كانت تُسقط «مَن سلّم» عند إقرار المستلِم، وهما **البصمتان** اللتان وُجد السجلُّ لهما.
--
-- ### ومفتاحا التوجيه — قرارٌ لا نسخ (وصفة §٤-٠ · الأنماطُ الثلاثة)
--  · **الفئة** بياناتٌ **مرجعيةٌ شبكية** (النمط ب): القاموسُ مغلقٌ مركزيٌّ يُدار للشبكة كلِّها
--    (ق-٦٤)، فنطاقُه **جذرُ الشبكة `/` صادقاً لا حشواً** (README الحسم ٢).
--  · **التسليم** بياناتٌ **تشغيليةٌ بالوحدة** (النمط أ)، وتوجيهُه **وحدةُ الوجهة**: ق-٦١
--    يوجب أن تكون الوجهةُ **محتواةً في المصدر احتواءً صارماً** (`contains(from, to)`) —
--    فالوجهةُ **أعمقُ المسارين دائماً**، وكلُّ نطاقٍ يحوي المصدرَ يحوي الوجهةَ ولا عكس.
--    فالتوجيهُ بالوجهة **أضيقُ مفتاحٍ صادقٍ يُبقي الطرفين قارئَين**، وهو نفسُه نطاقُ قيد
--    تدقيق التسليم (`box.handover` ⟵ `to.path`) فلا يُقرأ الحدثُ بلسانين.
--  · **ولا عمودَ `to_unit_path`**: هو `unit_path` بعينه — وعمودان لقيمةٍ واحدة مصدرا حقيقة.
-- ═══════════════════════════════════════════════════════════════════════════

-- ── القاموسُ المغلق المركزيّ (ق-٦٤): **بياناتٌ مرجعيةٌ تُدار ولا تُنشر كوداً** ────────
-- ولا اتحادَ نصوصٍ في الكود يوازيه: مصدرُ الحقيقة صفوفُ هذا الجدول لا قائمةٌ في مصدر.
CREATE TABLE IF NOT EXISTS box_categories (
  tenant_id  TEXT    NOT NULL,
  unit_path  TEXT    NOT NULL,
  id         TEXT    NOT NULL,
  ar         TEXT    NOT NULL,
  account_id TEXT    NOT NULL,
  active     INTEGER NOT NULL,
  PRIMARY KEY (tenant_id, id)
);
CREATE INDEX IF NOT EXISTS idx_box_categories_routing ON box_categories (tenant_id, unit_path);

-- ── سجلُّ التسليم النازل (ق-٦١): **توثيقُ البصمتين** — مَن سلّم ومَن أقرّ ──────────────
-- و`unit_path` هو **مسارُ وحدة الوجهة**؛ و`acknowledged_by`/`acknowledged_at` فارغان حتى
-- يقرّ أمينُ الوجهة **وحدَه** (خ-٧)، فالفراغُ هنا **حالٌ مُعلَنة** لا نقصُ بيانات.
CREATE TABLE IF NOT EXISTS box_handovers (
  tenant_id               TEXT    NOT NULL,
  unit_path               TEXT    NOT NULL,
  id                      TEXT    NOT NULL,
  entry_id                TEXT    NOT NULL,
  from_unit_path          TEXT    NOT NULL,
  to_custodian_person_id  TEXT    NOT NULL,
  handed_over_by          TEXT    NOT NULL,
  at                      INTEGER NOT NULL,
  acknowledged_by         TEXT,
  acknowledged_at         INTEGER,
  PRIMARY KEY (tenant_id, id)
);
CREATE INDEX IF NOT EXISTS idx_box_handovers_routing ON box_handovers (tenant_id, unit_path);

-- **وصفرُ فهرسٍ زائد — قرارٌ لا إغفال** (وصفة §٤-٤ · ADR-001 §١-٢: «الفهارسُ تكلّف قدر
-- الجدول تقريباً»). في العُهد كان لكلِّ فهرسٍ زائدٍ **قارئٌ حيٌّ يُسمّى**؛ وهنا **لا قارئ**:
-- كلُّ قراءةٍ في هذه الوحدة تمرّ على المستودع **بعد التحميل بالنطاق**، والتحميلُ بادئةُ
-- مسارٍ على `(tenant_id, unit_path)` — أي على فهرس التوجيه نفسِه. فحتى «ما ينتظر إقراري»
-- (`pendingHandoversFor`) **ترشيحٌ في الذاكرة** لا استعلامٌ بالشخص. وفهرسٌ بلا قارئٍ حيٍّ
-- **سعةٌ تُدفع اليوم لأملٍ في الغد** — وإن وُلد القارئُ فهجرةٌ تالية تُنشئه بدليله.
