-- 0002 — الرولّ-أب: رصيدُ الصندوق **تجميعاً مسبقاً** لا مجموعاً حيّاً
--
-- **هذا ملفٌّ لا يُعدَّل بعد الدمج** (المادة ٧/١): التصحيحُ بهجرةٍ جديدة.
-- **مسوّغُه**: ADR-001 ع-٦ («الرولّ-أب تجميعٌ مسبق حتماً، لا مجموعٌ حيّ») وقرارُ CR-026 (أ):
-- *«خرقُ ع-٦ نصّاً… ولا علاقةَ له بالتزامن؛ مجموعٌ حيٌّ على ٤٣ مليون سطرٍ يقتلنا متزامناً
-- كان أو غيرَ متزامن»*. وهو **مدخلُ الحارس الشرعيّ ق-٥٥** لا رقمٌ في تقرير.
--
-- **مفتاحُه: الشبكة × مفتاح التوجيه × الصندوق × العملة** — والمسوّغُ في `db/README.md`:
--   · **مفتاحُ التوجيه ضلعٌ في المفتاح لا عمودٌ مُرافق** (ع-٥ بلا استثناء): فتُقرأ صفوفُ
--     الرولّ-أب بنفس مُرشِّح الأسطر، ⟵ **المطابقةُ تصمد تحت أي نطاق** لا تحت الجذر وحده.
--   · **العملةُ ضلعٌ لا عمودٌ**: الأرصدةُ تُعرض بالعملات المنفصلة (ق-٦٢) — لا مكافئ يخفيها.
--
-- **لهجةُ القاسم المشترك** (ع-٣): لا `JSONB` · لا فهرسٌ جزئيّ · لا `AUTOINCREMENT`.
-- ═══════════════════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS fund_balances (
  tenant_id TEXT    NOT NULL,
  unit_path TEXT    NOT NULL,
  fund_id   TEXT    NOT NULL,
  currency  TEXT    NOT NULL,
  balance   INTEGER NOT NULL,
  PRIMARY KEY (tenant_id, unit_path, fund_id, currency)
);
CREATE INDEX IF NOT EXISTS idx_fund_balances_routing ON fund_balances (tenant_id, unit_path);
-- قراءةُ «رصيدُ هذا الصندوق بهذه العملة» عبر الشبكة — مسحُ بادئةٍ على فهرسٍ مركّب،
-- وهو الاستعلامُ الذي يستدعيه الحارسُ الشرعيُّ عند كلِّ صرف.
CREATE INDEX IF NOT EXISTS idx_fund_balances_fund ON fund_balances (tenant_id, fund_id, currency);
