/**
 * الواجهاتُ المستعارة — **اعتمادٌ بعقدٍ لا باستيراد وحدةٍ أخرى** (المادة ٣/٢، عقدُ الوحدة §٥).
 *
 * المعرضُ يجمع ما لا تملكه هذه الوحدة: صورَ سجل اليوم وصورَ الدروس (موطناهما وحدتاهما)،
 * و«مَن مسؤولُ الإعلام هنا؟» و«مَن مسؤولُ هذه الوحدة؟» (سؤالا **إسنادٍ** موطنُهما وحدةُ
 * التنظيم — ك-١٣). فتُعلَن هنا **واجهاتٍ تُحقن في سياق الطلب**، ولا تستورد الوحدةُ مجلدَ
 * وحدةٍ أخرى (قب-٣١: كلٌّ في مجلده).
 *
 * **والواجهاتُ تُعطي وقائعَ لا أحكاماً**: النسبةُ (ق-١٠٤) تُحسب في `gallery.ts` وحده، فتبقى
 * القاعدةُ في موضعٍ واحد ولا يقرّرها كلُّ رافدٍ باجتهاده (وهو عينُ مرض v1: المنطقُ مبعثر).
 */

/** صورةٌ من رافدٍ خارجيّ — أقلُّ ما يلزم للنسبة والعزل والترتيب، ولا بايتَ محتوى. */
export type FeedPhoto = {
  readonly id: string
  /** مسارُ وحدتها — أساسُ الترشيح الهابط (ق-١٧). */
  readonly unitPath: string
  readonly titleAr: string
  /** تاريخُ الوقوع لا الرفع (ق-١٠٣). */
  readonly occurredOn: Date
  /** رافعُها إن عُرف — و`null` مدخلٌ مشروعٌ يبدأ منه سُلَّمُ ق-١٠٤. */
  readonly uploaderPersonId: string | null
}

export type MediaPorts = {
  /** ق-١٠٦: مَن يحمل النشرَ في هذا النطاق — عددُهم يفرّق «شاغراً» من «خامل». */
  readonly officersIn: (unitPath: string) => readonly string[]
  /** ق-١٠٤: مسؤولُ الوحدة (أميرُ مسجدها) — الملاذُ قبل «غير منسوبة». */
  readonly responsibleFor: (unitPath: string) => string | null
  /** ق-١٠٤: أللرافعِ حسابٌ في الشبكة؟ فمَن لا حسابَ له لا يُنسب إليه. */
  readonly hasAccount: (personId: string) => boolean
  /** ق-١٠٤ الرافدان الآخران — بياناتٌ خامٌّ تُنسَب وتُرشَّح هنا. */
  readonly dailyLogPhotos: (unitPath: string) => readonly FeedPhoto[]
  readonly lessonPhotos: (unitPath: string) => readonly FeedPhoto[]
}
