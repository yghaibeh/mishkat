/**
 * الواجهاتُ المستعارة — **اعتمادٌ بعقدٍ لا باستيراد وحدةٍ أخرى** (المادة ٣/٢، عقدُ الوحدة §٢ و§٥).
 *
 * سؤالان لا تجيبهما هذه الوحدةُ بنفسها، وموطنُهما **وحدةُ التنظيم** (ك-١٣): «مَن المكلَّفُ عند
 * هذا المسار؟» و«أين أنت مُسنَد؟». فيُعلَنان هنا **واجهتَين تُحقنان في سياق الطلب** — بصفر
 * استيرادٍ من مجلد وحدةٍ أخرى (قب-٣١: كلٌّ في مجلده)، وبصفر استعلامٍ موازٍ في مستودعنا.
 *
 * > **والمنفذُ يُعطي وقائعَ لا أحكاماً** — وهذا ليس تفصيلاً بل **جوهرَ ق-٢٥**: لو رشّح
 * > `assignedAt` بنفسه منتهيَ التكليف لصار في النظام **فلترُ فعاليةٍ زمنيةٌ ثانٍ** بجانب
 * > `can()`، وهو **عينُ ما أنتج د-٢/د-٣ في v1**. فالمنفذُ ساذجٌ عمداً، والحكمُ للمحرّك وحده.
 */

import type { Actor } from "../../../authorization/can.js"

export type NotificationPorts = {
  /**
   * ق-١١/ق-٢٥ — المكلَّفون **عند هذا المسار بعينه** (لا ما فوقه ولا ما تحته).
   * **الأقربيّةُ تعيش هنا بنيةً**: مَن كُلِّف فوق الطبقة ليس مرشَّحاً أصلاً، فلا يحتاج
   * الأمرُ شرطاً يُنسى. والقائمةُ **خامٌّ**: بمنتهي التكليف والمعلَّقِ فيه — والمحرّكُ يبتّ.
   */
  readonly assignedAt: (scopePath: string) => readonly Actor[]

  /**
   * ح-٥ — مواضعُ إسناد الشخص: أساسُ **الحقّ المشتقّ** في قراءة إعلانات نطاقه
   * (`SPEC_authorization` §٢.١٣: «قراءةُ إعلانات نطاقك حقٌّ مشتقٌّ من الإسناد لا قدرةٌ تُمنح»).
   */
  readonly assignmentScopesOf: (personId: string) => readonly string[]
}
