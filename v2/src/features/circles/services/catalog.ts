/**
 * كتالوجُ أنواع الحلقات — **بياناتٌ مرجعية قابلة للتوسّع** (قب-٢٢، عقدُ الوحدة §٢).
 *
 * **معنى «منتج» هنا**: إضافةُ نوعٍ **صفٌّ يُحفظ**، لا سطرَ كودٍ يُنشر — فلا يحتاج المسجدُ
 * مبرمجاً ليفتح نوعاً جديداً. ولذلك **ليس في هذا الملفّ قائمةُ أنواعٍ ولا اسمُ نوعٍ واحد**:
 * كلُّها صفوفٌ في المستودع (نظيرُ كتالوج الأنشطة في T10 وقاموس فئات الصرف في T8).
 *
 * **وليس فيه سؤالُ «أمفعَّل؟»** — وهذا جوهرُ علاج ع-٨: في v1 كان النوعُ «قسماً يُفعَّل»
 * فصار **باباً ثانياً للمنع** فوق الصلاحية، فمُنع الأميرُ من إدارة حلقةٍ **يملكها**. وفي v2
 * السؤالُ واحدٌ لا ثانيَ له: **أهذا النوعُ موجود؟** — ولا حقلَ تفعيلٍ يُسأل عنه أصلاً.
 */

import type { CirclesStore } from "../data/store.js"
import type { CircleType } from "../types.js"

/**
 * الكتالوجُ كلُّه مرتّباً **حتمياً** بالمعرّف (درسُ لا-حتميّة الترتيب في v1).
 * **ولا مرشّحَ تفعيلٍ هنا**: كلُّ صفٍّ في الكتالوج نوعٌ صالحٌ للاستعمال بحكم وجوده.
 */
export function allTypes(store: CirclesStore): readonly CircleType[] {
  return [...store.types()].sort((a, b) => a.id.localeCompare(b.id))
}

/** نوعٌ بمعرّفه — و**المجهولُ `null` لا استثناء**: قيمةٌ معلنةٌ يبني عليها المستدعي رفضاً مصنَّفاً. */
export function typeById(store: CirclesStore, typeId: string): CircleType | null {
  return store.getType(typeId)
}
