/**
 * **منفذا الجلسة الموحّدة** — ما تعلنه هذه الوحدةُ سؤالاً ولا تستورده كياناً (CR-016).
 *
 * الجلسةُ اليومية **كيانٌ واحدٌ صاحبُه هذه الوحدة**، لكنّ **شكلَ حقوله يتبع نوعَ الحلقة**؛
 * ومعرفةُ «أيُّ شكلٍ لأيّ نوع؟» و«أمقفلةٌ هذه الجلسة؟» **ليستا من علم صاحب الكيان**:
 *
 *  ١. **الشكلُ من البيانات لا من الكود** (قب-٤٠): أنواعُ الحلقات **صفوفٌ لكل شبكة** لا ثوابتُ
 *     كود، فربطُ نوعٍ بمنهاجٍ **صفٌّ** كذلك. ولو سرَدَت هذه الوحدةُ أنواعاً بأسمائها لأعادت
 *     بناء ع-٨ حرفياً («نوعٌ يعرفه الكودُ وآخرُ لا يعرفه»). فتسأل المنفذَ، **ويجيب صاحبُ
 *     الكتالوج بما عنده من صفوف**.
 *  ٢. **القفلُ حالٌ لا سلسلة** (G22، نظيرُ منفذ القفل في T10): تعرف الوحدةُ أنّ جلسةً
 *     **مقفلة** فتمنع الكتابةَ عليها، ولا تعرف **مَن أقفلها ولا بأيّ سلسلة** — فلا يتسرّب
 *     منطقُ الاعتماد إلى وحدة ميزة.
 *
 * **والاتجاهُ واحدٌ صارم**: هذا الملفُّ **يعلن ولا يستورد** — لا سطرَ واحدٌ فيه يعرف وحدةً
 * أخرى. ومَن ينفّذ المنفذَ يعيش **في وحدته هو**، فيبقى الاعتمادُ بين الوحدتين في اتجاهٍ واحد
 * ولا تُخلق حلقةُ استيرادٍ مغلقة.
 */

/** شكلا الجلسة — وهما **اثنان لأنّ حقولهما اثنتان في الكود**، لا لأنّ الأنواعَ اثنان. */
export type SessionShapeKind = "recitation" | "curriculum"

export type SessionShapePort = {
  /** «أيَّ شكلٍ تأخذ جلسةُ حلقةٍ من هذا النوع؟» — جوابٌ **من صفوفٍ** لا من قائمةٍ في الكود. */
  readonly shapeOf: (circleTypeId: string) => SessionShapeKind
}

/**
 * **التركيبُ الأدنى**: شبكةٌ بلا كتالوج مناهجَ ⇒ **كلُّ جلساتها تحفيظ**.
 *
 * وهو الجوابُ الصحيح لا الجوابُ المتساهل: لا منهاجَ ⇒ لا مجلسَ ⇒ لا شكلَ منهاجٍ يُسجَّل؛
 * ومحاولةُ تسجيله تُردّ بـ`SESSION_SHAPE_MISMATCH` بدل أن تُقبل صامتةً.
 */
export const RECITATION_SHAPE_ONLY: SessionShapePort = Object.freeze({
  shapeOf: (): SessionShapeKind => "recitation",
})

/**
 * «أمقفلةٌ هذه الجلسة؟» — **سؤالٌ عن حالٍ** (ق-٨): المقفلةُ لا يُكتب عليها.
 * وافتراضُه في كل سياقٍ **لا**: القفلُ **فعلٌ يقع** لا حالةٌ ضمنية.
 */
export type SessionLockPort = (sessionId: string) => boolean

/** التركيبُ الأدنى: لا مُقفِلَ موصولٌ ⇒ **لا جلسةَ مقفلة** — سلوكُ T18 قبل التوحيد بحرفه. */
export const NEVER_LOCKED: SessionLockPort = () => false
